#!/usr/bin/env node

import { createHash } from "node:crypto";
import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const repoRoot = process.cwd();
const avatarRoot = path.join(repoRoot, "public", "avatars");
const outputPath = path.join(repoRoot, "src", "lib", "avatar-asset-versions.ts");
const cacheBustedExtensions = new Set([".mp4", ".webp"]);

async function listAvatarAssets(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const assets = [];

  for (const entry of entries) {
    if (entry.name.startsWith(".")) {
      continue;
    }

    const filePath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      assets.push(...(await listAvatarAssets(filePath)));
      continue;
    }

    if (entry.isFile() && cacheBustedExtensions.has(path.extname(entry.name))) {
      assets.push(filePath);
    }
  }

  return assets;
}

function toPublicPath(filePath) {
  return `/${path.relative(path.join(repoRoot, "public"), filePath).split(path.sep).join("/")}`;
}

async function hashFile(filePath) {
  const buffer = await readFile(filePath);

  return createHash("sha256").update(buffer).digest("hex").slice(0, 16);
}

const assets = await listAvatarAssets(avatarRoot);
const versions = Object.fromEntries(
  await Promise.all(
    assets.sort((a, b) => toPublicPath(a).localeCompare(toPublicPath(b))).map(async (filePath) => [
      toPublicPath(filePath),
      await hashFile(filePath),
    ]),
  ),
);

const lines = [
  "/* This file is generated by scripts/generate-avatar-asset-versions.mjs. */",
  "",
  "export const AVATAR_ASSET_VERSIONS = {",
  ...Object.entries(versions).map(([assetPath, version]) => `  ${JSON.stringify(assetPath)}: ${JSON.stringify(version)},`),
  "} as const;",
  "",
  "export type AvatarAssetPath = keyof typeof AVATAR_ASSET_VERSIONS;",
  "",
];

await writeFile(outputPath, `${lines.join("\n")}`);
